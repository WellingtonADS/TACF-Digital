CREATE OR REPLACE FUNCTION public.get_results_history(p_limit integer, p_cursor text, p_from date DEFAULT NULL::date, p_to date DEFAULT NULL::date)
 RETURNS jsonb
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
AS $function$
DECLARE
  v_rows jsonb;
  v_has_more boolean := false;
  v_next_cursor text := NULL;
  v_arg jsonb;
BEGIN
  -- parse cursor if present
  IF p_cursor IS NOT NULL THEN
    v_arg := convert_from(decode(p_cursor, 'base64'), 'utf8')::jsonb;
    SELECT jsonb_agg(row_to_json(t)) INTO v_rows
    FROM (
      SELECT b.id, b.user_id AS profile_id, p.full_name, p.saram, b.score, b.result_details, b.test_date, b.created_at
      FROM public.bookings b
      LEFT JOIN public.profiles p ON p.id = b.user_id
      WHERE (b.created_at, b.id) < ((v_arg->>'created_at')::timestamptz, (v_arg->>'id')::uuid)
        AND (p_from IS NULL OR b.test_date >= p_from)
        AND (p_to IS NULL OR b.test_date <= p_to)
        AND b.user_id = auth.uid()
      ORDER BY b.created_at DESC, b.id DESC
      LIMIT p_limit
    ) t;
  ELSE
    SELECT jsonb_agg(row_to_json(t)) INTO v_rows
    FROM (
      SELECT b.id, b.user_id AS profile_id, p.full_name, p.saram, b.score, b.result_details, b.test_date, b.created_at
      FROM public.bookings b
      LEFT JOIN public.profiles p ON p.id = b.user_id
      WHERE (p_from IS NULL OR b.test_date >= p_from)
        AND (p_to IS NULL OR b.test_date <= p_to)
        AND b.user_id = auth.uid()
      ORDER BY b.created_at DESC, b.id DESC
      LIMIT p_limit
    ) t;
  END IF;

  -- Normalize empty result
  IF v_rows IS NULL OR v_rows = 'null'::jsonb THEN
    v_rows := '[]'::jsonb;
    v_has_more := false;
    v_next_cursor := NULL;
  ELSE
    IF jsonb_array_length(v_rows) = p_limit THEN
      v_has_more := true;
      v_next_cursor := (
        SELECT encode(convert_to(jsonb_build_object('created_at', (elem->>'created_at')::text, 'id', elem->>'id')::text, 'utf8'), 'base64')
        FROM jsonb_array_elements(v_rows) WITH ORDINALITY arr(elem, idx)
        WHERE idx = jsonb_array_length(v_rows)
        LIMIT 1
      );
    ELSE
      v_has_more := false;
      v_next_cursor := NULL;
    END IF;
  END IF;

  RETURN jsonb_build_object('rows', v_rows, 'next_cursor', v_next_cursor, 'has_more', v_has_more);
END;
$function$


CREATE OR REPLACE FUNCTION public.get_sessions_availability(p_start date, p_end date)
 RETURNS TABLE(session_id uuid, date date, period text, max_capacity integer, occupied_count bigint, available_count bigint)
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
AS $function$
BEGIN
  RETURN QUERY
  SELECT
    s.id,
    s.date,
    s.period::text,
    s.max_capacity,
    COUNT(b.id) AS occupied_count,
    (s.max_capacity - COUNT(b.id)) AS available_count
  FROM public.sessions s
  LEFT JOIN public.bookings b
    ON b.session_id = s.id AND b.status::text IN ('agendado', 'remarcado')
  WHERE s.date >= p_start AND s.date <= p_end
  GROUP BY s.id, s.date, s.period, s.max_capacity
  ORDER BY s.date;
END;
$function$


CREATE OR REPLACE FUNCTION public.get_user_dashboard_summary()
 RETURNS jsonb
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
AS $function$
DECLARE
  v_bookings_count integer := 0;
  v_results_count integer := 0;
  v_next RECORD;
  v_latest_order text := NULL;
  v_result jsonb;
BEGIN
  -- Count confirmed bookings for the current user
  SELECT COUNT(*) INTO v_bookings_count
  FROM public.bookings b
  WHERE b.user_id = auth.uid()::uuid
    AND b.status = 'confirmed';

  -- Count bookings that have a score (results)
  SELECT COUNT(*) INTO v_results_count
  FROM public.bookings b
  WHERE b.user_id = auth.uid()::uuid
    AND b.score IS NOT NULL;

  -- Next session (by date) for confirmed bookings from today onwards
  SELECT s.id AS session_id, s.date, s.period, s.max_capacity
  INTO v_next
  FROM public.bookings b
  JOIN public.sessions s ON s.id = b.session_id
  WHERE b.user_id = auth.uid()::uuid
    AND b.status = 'confirmed'
    AND s.date >= current_date
  ORDER BY s.date ASC
  LIMIT 1;

  -- Latest order number, if any (use most recent confirmed booking that has an order_number)
  SELECT b.order_number INTO v_latest_order
  FROM public.bookings b
  WHERE b.user_id = auth.uid()::uuid
    AND b.status = 'confirmed'
    AND b.order_number IS NOT NULL
  ORDER BY b.created_at DESC
  LIMIT 1;

  v_result := jsonb_build_object(
    'bookings_count', COALESCE(v_bookings_count, 0),
    'results_count', COALESCE(v_results_count, 0),
    'next_session', CASE WHEN v_next IS NULL THEN NULL ELSE jsonb_build_object('session_id', v_next.session_id, 'date', v_next.date, 'period', v_next.period, 'max_capacity', v_next.max_capacity) END,
    'latest_order_number', v_latest_order
  );

  RETURN v_result;
END;
$function$


