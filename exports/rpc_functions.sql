 RETURNS TABLE(success boolean, error text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_request RECORD;
  v_max integer;
  v_count integer;
BEGIN
  SELECT * INTO v_request FROM public.swap_requests WHERE id = p_request_id FOR UPDATE;
  IF NOT FOUND THEN
    RETURN QUERY SELECT false, 'request not found';
    RETURN;
  END IF;

  -- Load session capacity for new session and lock
  SELECT max_capacity INTO v_max FROM public.sessions WHERE id = v_request.new_session_id FOR UPDATE;
  IF NOT FOUND THEN
    RETURN QUERY SELECT false, 'new session not found';
    RETURN;
  END IF;

  SELECT COUNT(*) INTO v_count FROM public.bookings WHERE session_id = v_request.new_session_id AND status = 'confirmed';
  IF v_count >= v_max THEN
    RETURN QUERY SELECT false, 'new session full';
    RETURN;
  END IF;

  -- Update booking to new session
  UPDATE public.bookings
  SET session_id = v_request.new_session_id, status = 'confirmed', updated_at = NOW()
  WHERE id = v_request.booking_id;

  -- Update swap request as processed
  UPDATE public.swap_requests
  SET status = 'approved', processed_by = p_admin_id, processed_at = NOW(), updated_at = NOW()
  WHERE id = p_request_id;

  RETURN QUERY SELECT true::boolean, NULL::text;

EXCEPTION WHEN others THEN
  RETURN QUERY SELECT false::boolean, SQLERRM::text;
END;
$function$


CREATE OR REPLACE FUNCTION public.book_session(p_user_id uuid, p_session_id uuid)
 RETURNS TABLE(success boolean, booking_id uuid, error text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_max integer;
  v_count integer;
  v_booking_id uuid;
  v_semester semester_type;
  v_existing_semester_bookings integer;
  v_session_date date;
  v_role user_role;
  v_active boolean;
BEGIN
  -- Lock the session row to serialize concurrent checks
  SELECT max_capacity, date INTO v_max, v_session_date
  FROM public.sessions
  WHERE id = p_session_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'session not found'::text;
    RETURN;
  END IF;

  -- Verify caller is booking for themselves
  IF auth.uid() IS NULL OR auth.uid()::uuid <> p_user_id THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'unauthorized: user mismatch'::text;
    RETURN;
  END IF;

  -- Determine user's role/active status
  SELECT role, active INTO v_role, v_active FROM public.profiles WHERE id = p_user_id;

  IF v_active IS FALSE THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'profile inactive'::text;
    RETURN;
  END IF;

  -- Admins and coordinators are not allowed to book
  IF v_role IN ('admin', 'coordinator') THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'role not allowed to book'::text;
    RETURN;
  END IF;

  IF v_session_date IS NULL THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'session date unknown'::text;
    RETURN;
  END IF;

  -- Semester is defined by session calendar date
  v_semester := CASE
    WHEN EXTRACT(MONTH FROM v_session_date) <= 6 THEN '1'::semester_type
    ELSE '2'::semester_type
  END;

  SELECT COUNT(*) INTO v_existing_semester_bookings
  FROM public.bookings b
  WHERE b.user_id = p_user_id AND b.status = 'confirmed' AND b.semester = v_semester;

  IF v_existing_semester_bookings > 0 THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'user already has booking this semester'::text;
    RETURN;
  END IF;

  -- Count confirmed bookings for the session
  SELECT COUNT(*) INTO v_count
  FROM public.bookings
  WHERE session_id = p_session_id AND status = 'confirmed';

  IF v_count >= v_max THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'session full'::text;
    RETURN;
  END IF;

  -- Create booking; set semester accordingly
  INSERT INTO public.bookings (user_id, session_id, status, semester)
  VALUES (p_user_id, p_session_id, 'confirmed', v_semester)
  RETURNING id INTO v_booking_id;

  RETURN QUERY SELECT true::boolean, v_booking_id, NULL::text;

EXCEPTION
  WHEN unique_violation THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, 'duplicate booking'::text;
  WHEN others THEN
    RETURN QUERY SELECT false::boolean, NULL::uuid, SQLERRM::text;
END;
$function$


CREATE OR REPLACE FUNCTION public.confirmar_agendamento(p_user_id uuid, p_session_id uuid)
 RETURNS TABLE(success boolean, booking_id uuid, error text, order_number text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
    v_session_date DATE;
    v_max_cap INTEGER;
    v_current_cap INTEGER;
    v_session_status public.session_status;
    v_semester public.semester_type;
    v_year INTEGER;
    v_next_order INTEGER;
    v_order_str TEXT;
    v_new_booking_id UUID;
    v_profile_active BOOLEAN;
BEGIN
    -- 1. Bloqueio pessimista e leitura da capacidade (O(1))
    SELECT starts_at::date AS date, max_capacity, capacity, status 
    INTO v_session_date, v_max_cap, v_current_cap, v_session_status
    FROM public.sessions 
    WHERE id = p_session_id 
    FOR UPDATE;

    -- 2. Validações de Negócio
    IF NOT FOUND THEN
        RETURN QUERY SELECT false, NULL::UUID, 'session not found'::TEXT, NULL::TEXT;
        RETURN;
    END IF;

    IF v_session_status != 'open' THEN
        RETURN QUERY SELECT false, NULL::UUID, 'session not open'::TEXT, NULL::TEXT;
        RETURN;
    END IF;

    -- Verificação instantânea via Counter Cache
    IF v_current_cap >= v_max_cap THEN
        RETURN QUERY SELECT false, NULL::UUID, 'session full'::TEXT, NULL::TEXT;
        RETURN;
    END IF;

    -- 3. Verificação de Perfil
    SELECT active INTO v_profile_active FROM public.profiles WHERE id = p_user_id;
    IF v_profile_active IS NOT TRUE THEN
        RETURN QUERY SELECT false, NULL::UUID, 'profile inactive'::TEXT, NULL::TEXT;
        RETURN;
    END IF;

    -- 4. Cálculo de Semestre e Ano
    v_year := EXTRACT(YEAR FROM v_session_date);
    v_semester := CASE WHEN EXTRACT(MONTH FROM v_session_date) <= 6 THEN '1' ELSE '2' END;

    -- Evita duplicidade no mesmo semestre
    IF EXISTS (
        SELECT 1 FROM public.bookings 
        WHERE user_id = p_user_id 
        AND semester = v_semester 
        AND test_date >= DATE_TRUNC('year', v_session_date)
        AND status = 'agendado'
    ) THEN
        RETURN QUERY SELECT false, NULL::UUID, 'user already has booking this semester'::TEXT, NULL::TEXT;
        RETURN;
    END IF;

    -- 5. Geração Atómica do Número de Ordem
    INSERT INTO public.order_numbers (year, semester, last)
    VALUES (v_year, v_semester::text, 1)
    ON CONFLICT (year, semester) 
    DO UPDATE SET last = order_numbers.last + 1
    RETURNING last INTO v_next_order;

    v_order_str := v_year || '-' || v_semester || '-' || LPAD(v_next_order::TEXT, 4, '0');

    -- 6. Inserção Única (A trigger tr_sync_session_capacity atualizará a contagem)
    INSERT INTO public.bookings (
        user_id, 
        session_id, 
        status, 
        semester, 
        order_number, 
        test_date
    ) VALUES (
        p_user_id, 
        p_session_id, 
        'agendado', 
        v_semester, 
        v_order_str, 
        v_session_date
    ) RETURNING id INTO v_new_booking_id;

    RETURN QUERY SELECT true, v_new_booking_id, NULL::TEXT, v_order_str;

EXCEPTION WHEN OTHERS THEN
    RETURN QUERY SELECT false, NULL::UUID, SQLERRM, NULL::TEXT;
END;
$function$


